# Strings

name = "Andrew"
print (name)

# Adding in plurals / ownership
# use the "" which will then let you also use '
name = "Andrew's Code"
print (name)

# or alternatively add in \ - which means that you really do mean the next part to be there ie '
name = 'Andrew\'s code'
print (name)

# cannot add numbers / intergers or floats unless you change them to a str
name = 'Andrew\'s code ' + str(1)
print (name)

